/**
 * キーワードが暑さ関連かどうかを判定する
 */
export function isHeatRelatedKeyword(keyword: string): boolean {
  if (!keyword) return false;
  const normalized = keyword.toLowerCase().replace(/\s+/g, ' ');
  return HEAT_KEYWORDS.some((hk) => {
    // スペース区切りのパターンは両方の単語が含まれているかチェック
    if (hk.includes(' ')) {
      const parts = hk.split(' ');
      return parts.every((part) => normalized.includes(part));
    }
    return normalized.includes(hk);
  });
}

